public class UrInfo {
    private int uid;
    private int rid;

    public int getRid() {
        return rid;
    }

    public void setRid(int rid) {
        this.rid = rid;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    @Override
    public String toString() {
        return "UrInfo{" +
                "uid=" + uid +
                ", rid=" + rid +
                '}';
    }
}
